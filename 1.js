// Разбор решения:
//     Используемый паттерн: Hash Map (Хеш-таблица)
// Алгоритм использует хеш-таблицу (numMap) для хранения чисел и их индексов, что позволяет достичь оптимальной сложности.
//
//     Шаги работы:
//     Инициализация:
//
//         numMap = {} — пустой объект для хранения чисел и их индексов.
//
//     Проход по массиву (for):
//
// Для каждого числа nums[i] вычисляется complement = target - nums[i] (число, которое нужно найти для текущего, чтобы их сумма давала target).
//
// Проверка наличия complement в numMap:
//
//     Если complement есть в numMap → значит, мы нашли пару.
//     Возвращаем [индекс complement, текущий индекс].
//
// Если нет → сохраняем текущее число и его индекс в numMap.
//
//     Если решение не найдено (по условию задачи всегда есть):
//
// Возвращаем [] (но в корректных входных данных сюда не дойдем).

function twoSum(nums, target) {
    // Создаем объект для хранения чисел и их индексов
    const numMap = {};

    // Проходим по всем элементам массива
    for (let i = 0; i < nums.length; i++) {
        // Вычисляем какое число нам нужно для текущего элемента
        const complement = target - nums[i];

        // Проверяем, есть ли нужное число в нашем объекте
        if (complement in numMap) {
            // Если есть - возвращаем индексы
            return [numMap[complement], i];
        }

        // Если нет - сохраняем текущее число и его индекс
        numMap[nums[i]] = i;
    }

    return []; // По условию сюда никогда не дойдем
}